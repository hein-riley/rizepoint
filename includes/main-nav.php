<script>
$(document).ready(function() {
	// pull up all subnavs so they're not extended at start
	jQuery('.subnav').slideUp();
	var pain = jQuery('.subnav:last-child');
	
	if(pain.val == 'display: block'){
		pain.css("display","none");
	}else if(pain.val == 'display: none'){
		pain.click(function(){
			pain.css("display","block");
		});
	}
	//check for main url
	jQuery('.parent a').each(function() {
		if (window.location.href.indexOf($(this).prop('href')) != -1) {
		  // jQuery(this).addClass('current');
		   jQuery(this).closest('.parent').find('ul.subnav').slideDown();
		} 
	});
	//hover slidedown and slideups
	jQuery('li.parent').hover(function() {
		jQuery(this).siblings().find('.subnav').slideUp();
		jQuery(this).children('.subnav').slideDown();
	});
});
</script>

